#import "JYBIChatMessage.h"

@implementation JYBIChatMessage

- (id)copyWithZone:(NSZone *)zone
{
    JYBIChatMessage *messageCopy = [[self class] allocWithZone:zone];
    messageCopy.commandID = self.commandID;
    messageCopy.timestamp = self.timestamp;
    messageCopy.messageID = self.messageID;
    messageCopy.fromUserID = self.fromUserID;
    messageCopy.userName = self.userName;
    messageCopy.toUserID = self.toUserID;
    messageCopy.conversationChatter = self.conversationChatter;
    messageCopy.nodeId = self.nodeId;
    messageCopy.messageBodyType = self.messageBodyType;
    messageCopy.messageType = self.messageType;
    messageCopy.messageActionStatus = self.messageActionStatus;
    messageCopy.content = self.content;
    messageCopy.duration = self.duration;
    messageCopy.remoteUrl = self.remoteUrl;
    messageCopy.isPlaying = self.isPlaying;
    messageCopy.isPlayed = self.isPlayed;
    messageCopy.image = self.image;
    messageCopy.imageSize = self.imageSize;
    messageCopy.videoThumbnailPath = self.videoThumbnailPath;
    messageCopy.localPath = self.localPath;
    messageCopy.fileSize = self.fileSize;
    messageCopy.isRead = self.isRead;
    return messageCopy;
}
@end
